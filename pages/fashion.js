import Head from 'next/head';
import React, { useEffect, useState } from 'react'
import { Footer } from '../components/Footer';
import HeaderItems from '../components/HeaderItems';
import ItemSets from '../components/ItemSets';
import Navbar from '../components/Navbar';
import { db } from '../Firebase';
import { FAd, FAd1, FAd2, FAd3 } from '../BannerImages'
import Ads from '../components/Ads';

const Fashion = () => {
    const [productData, setProductData] = useState([])
    const Images = [FAd, FAd1, FAd2, FAd3]

    useEffect(() => {
        db.collection("FashionData").onSnapshot((snapshot) => {
            // eslint-disable-next-line array-callback-return
            snapshot.docs.map((doc) => {
                var data = doc.data()
                setProductData(product => [...product, data])

            });
        });
    }, []);

    return (
        <div>
            <Head>
                <title>Fashion</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <Navbar />
            <HeaderItems />

            <Ads images={Images} />

            <div className="relative top-[18rem] md:top-[28rem] py-8">
                <ItemSets productData={productData} />
                <Footer />
            </div>

        </div>
    )
}

export default Fashion